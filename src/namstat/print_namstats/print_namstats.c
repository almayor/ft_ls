#include "ft_ls.h"

void		print_namstats(t_namstat **nst, const t_options *opt)
{
	while (*nst)
	{
		if (opt->a || (*nst)->base[0] != '.')
			ft_printf("%s\n", (*nst)->base);
		nst++;
	}
}
